package main

import "fmt"

func main() {
	// Создание переменных с использованием краткой формы объявления
	age := 21         // int
	height := 1.92    // float64
	name := "Ilia"    // string
	isStudent := true // bool
	nameuni := "PSUTI" // string 2

	// Вывод переменных на экран
	fmt.Println("Имя:", name)
	fmt.Println("Возраст:", age)
	fmt.Println("Рост:", height)
	fmt.Println("Студент:", isStudent)
	fmt.Println("Универститет:", nameuni)
}
